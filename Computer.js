class Computer {
  constructor(
    computerId,
    monitor,
    cpu,
    ram,
    storageType,
    storageCapacity,
    status
  ) {
    this.computerId = computerId;
    this.monitor = monitor;
    this.cpu = cpu;
    this.ram = ram;
    this.storageType = storageType;
    this.storageCapacity = storageCapacity;
    this.status = status;
  }
}
